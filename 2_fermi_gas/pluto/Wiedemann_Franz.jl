md"""
# Plot thermal and electrical conductivity of copper to demonstrate Wiedemann-Franz law
"""

# ╔═╡ 6e99d03f-5fb0-4ae0-8246-06dc37f7472c
md"""
read data from Hust, Jerome G and Lankford, Alan B: 
Thermal conductivity of aluminum, copper, iron, and tungsten for temperatures from 1 K to the melting point, National Bureau of Standards, Boulder, CO (USA). NBSIR 84-3007 (1984)
"""

# ╔═╡ 9077a1f8-c1d3-4a0e-9d3a-abfa18ff63fa
begin
	d1 = CSV.read("CU_K_T.csv", DataFrame)
	rename!(d1, :x => :T, :Curve1 => :K)
	d1.K = d1.K .* 100    # K was plotted scaled by 100
	
	d2 = CSV.read("CU_sigma_T.csv", DataFrame)
	rename!(d2, :x => :T, :Curve1 => :R)
	d2.σ = 1 ./ d2.R     # calculate sigma from R
end;

# ╔═╡ 0815256b-eaa4-430f-9586-0215f13d5b0a
md"""
interpolate K and σ to calculate L from it. Evaluate L at some points, equally spaced on log scale.
"""

# ╔═╡ c2a0c207-e732-44f9-b50b-d113685a37f7
begin
	K_interp = linear_interpolation(d1.T, d1.K ,extrapolation_bc=Line()) 
	σ_interp = linear_interpolation(d2.T, d2.σ ,extrapolation_bc=Line()) 
	
	T = 10 .^ (range(0.3,2.9,20))
	L = @. K_interp(T) / (T * σ_interp(T))
end;

# ╔═╡ 903eb832-80f1-43ae-ae80-804276e5905e
md"""
define 2 temperature intervalls to plot asymptotes
"""

# ╔═╡ f1bfcefb-1eba-484e-80ba-6c743feff614
begin
	Trange_high = [1e2, 2e3]
	Trange_low = [1.5,20]
end;

# ╔═╡ 1b9b77a3-e792-43f1-8413-a19061e6882c
md"""
plot everything via pgfplot for Latex
"""

# ╔═╡ 1c79530d-b0b3-4d87-ae20-90b6b891136f
myplot = @pgf TikzPicture(
        LogLogAxis(
			{
              xlabel = "Temperatur T (K)",
              ylabel = raw"K ($W/ K m$),  $\sigma$ ($10^{-9} /\Omega m$)",
			 raw"axis y line* = {left}",
			xmin = 1, xmax = 2000,
			ymin = 0.8e-2, ymax = 5e4,
			width= "80mm", height= "40mm", 
			scale_only_axis=true,
          },
            PlotInc({ only_marks },
                Table(; x = d1.T, y = d1.K)),
            PlotInc({ only_marks },
                Table(; x = d2.T, y = d2.σ)),
		raw"\node at (200,5) {L};",
		raw"\draw[->] (200,10) -- (300,10);",

		raw"\node at (3,50) {$\sigma$};",
		raw"\draw[->] (3,30) -- (2,30);",

		raw"\node at (30,1000) {K};",
		raw"\draw[->] (30,500) -- (20,500);",


 			PlotInc({ no_marks, solid, black },
                Table(; x =  Trange_high, y = 1.5e1 ./ Trange_high)),
 			PlotInc({ no_marks, solid, black },
                Table(; x =  Trange_high, y = 4e2 .* Trange_high.^0)),
			PlotInc({ no_marks, solid, black },
                Table(; x =  Trange_low, y = 2.5e3 .* Trange_low)),
 			PlotInc({ no_marks,solid,black },
                Table(; x =  Trange_low, y = 9e1 .* Trange_low.^0)),

		raw"\node at (500,0.15) {$T^{-1}$};",
		raw"\node at (7,5000) {$T^1$};",


)	,
        SemiLogXAxis( {xmin = 1, xmax = 2000,
              ylabel = raw"Lorenz-Zahl L ($10^{-9} W \Omega / K^2$)",
			 raw"axis y line* = {right}",
			ymin = 0, ymax = 40,
			width= "80mm", height= "40mm", 
			scale_only_axis=true,
          },
            PlotInc({ only_marks, mark = {o} },
                Table(; x = T, y = L)
		)
),
)

